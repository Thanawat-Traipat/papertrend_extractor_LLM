# TAB 5: PAPER EXPLORER
# ═══════════════════════════════════════════════════════════════════
with tab_explorer:
    st.header("Paper Explorer")

    df_t = filter_by_sidebar(df_trends)

    if df_t is None or df_t.empty:
        st.info("No data for the selected filters.")
    else:
        # --- Paper search ---
        paper_search = st.text_input("Search by title", "", placeholder="e.g. peer feedback, EFL...")

        papers = df_t.groupby(["paper_id", "year", "title"]).agg(
            topics=("topic", lambda x: ", ".join(x.unique())),
            keywords=("keyword", lambda x: ", ".join(x.unique())),
            kw_count=("keyword", "nunique"),
        ).reset_index().sort_values("year", ascending=False)

        # Merge track info if available
        if df_single is not None:
            df_s_filt = filter_by_sidebar(df_single)
            if df_s_filt is not None and not df_s_filt.empty:
                track_labels = []
                for _, row in df_s_filt.iterrows():
                    tracks = [t for t in TRACK_COLS if row.get(t, 0) == 1]
                    track_labels.append({
                        "paper_id": row["paper_id"],
                        "track": ", ".join(tracks) if tracks else "—",
                    })
                if track_labels:
                    tl_df = pd.DataFrame(track_labels)
                    papers = papers.merge(tl_df, on="paper_id", how="left")

        if paper_search:
            papers = papers[papers["title"].str.contains(paper_search, case=False, na=False)]

        st.dataframe(
            papers.rename(columns={
                "paper_id": "ID",
                "year": "Year",
                "title": "Title",
                "topics": "Topics",
                "keywords": "Keywords",
                "kw_count": "# Keywords",
                "track": "Track",
            }),
            use_container_width=True,
            height=500,
        )

        # --- Paper detail view ---
        st.subheader("Paper Detail View")
        paper_ids = papers["paper_id"].unique().tolist()
        if paper_ids:
            selected_pid = st.selectbox("Select a paper ID", paper_ids, key="paper_detail_select")
            paper_rows = df_t[df_t["paper_id"] == selected_pid]
            if not paper_rows.empty:
                st.markdown(f"### {paper_rows.iloc[0]['title']}")
                st.markdown(f"**Year:** {paper_rows.iloc[0]['year']}")

                # Track badge
                if df_single is not None:
                    track_row = df_single[df_single["paper_id"] == selected_pid]
                    if not track_row.empty:
                        tracks = [t for t in TRACK_COLS if track_row.iloc[0].get(t, 0) == 1]
                        if tracks:
                            st.markdown("**Track(s):** " + " / ".join(tracks))

                # Keywords + evidence table
                detail = paper_rows[["keyword", "keyword_frequency", "topic", "evidence"]].copy()
                detail.columns = ["Keyword", "Frequency", "Topic", "Evidence (Verbatim)"]
                st.dataframe(detail, use_container_width=True)


