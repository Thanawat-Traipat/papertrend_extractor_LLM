        st.caption("Compares the first half vs second half of the selected year range.")

        sorted_yrs = sorted(df_t["year"].unique())
        if len(sorted_yrs) >= 2:
            mid = len(sorted_yrs) // 2
            early_years = sorted_yrs[:mid]
            late_years = sorted_yrs[mid:]

            early = df_t[df_t["year"].isin(early_years)].groupby("topic")["paper_id"].nunique()
            late = df_t[df_t["year"].isin(late_years)].groupby("topic")["paper_id"].nunique()
            shift = (late.reindex(early.index.union(late.index), fill_value=0) -
                     early.reindex(early.index.union(late.index), fill_value=0))

            emerging = shift.nlargest(8).reset_index()
            emerging.columns = ["Topic", "Change"]
            declining = shift.nsmallest(8).reset_index()
            declining.columns = ["Topic", "Change"]

            e_col, d_col = st.columns(2, gap="large")
            with e_col:
                fig_e = px.bar(
                    emerging, x="Change", y="Topic", orientation="h",
                    title=f"Emerging ({early_years[0]} – {late_years[-1]})",
                    color_discrete_sequence=["#3cba83"],
                )
                fig_e.update_layout(margin=dict(l=10, r=20, t=50, b=20), plot_bgcolor="#fafbfd")
                st.plotly_chart(fig_e, use_container_width=True)
            with d_col:
                fig_d = px.bar(
                    declining, x="Change", y="Topic", orientation="h",
                    title=f"Declining ({early_years[0]} – {late_years[-1]})",
                    color_discrete_sequence=["#e05c5c"],
                )
                fig_d.update_layout(margin=dict(l=10, r=20, t=50, b=20), plot_bgcolor="#fafbfd")
