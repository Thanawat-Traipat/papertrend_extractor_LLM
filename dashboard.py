def safe_years(df):
    """Return sorted unique years from a dataframe."""
    if df is None:
        return []
    return sorted(df["year"].unique().tolist())

def build_track_long(df_track):
    """Melt track one-hot df into long format for easy plotting."""
    rows = []
    for _, r in df_track.iterrows():
        for t in TRACK_COLS:
            if r.get(t, 0) == 1:
                rows.append({"paper_id": r["paper_id"], "year": r["year"],
                             "title": r["title"], "track": t})
    return pd.DataFrame(rows)

def filter_by_sidebar(df, year_col="year"):
    """Apply the global sidebar filters (year range + track) to a df."""
    if df is None or df.empty:
        return df
    mask = df[year_col].isin(selected_years) if selected_years else pd.Series([True]*len(df))
    return df[mask]


